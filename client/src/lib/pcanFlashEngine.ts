/**
 * DevProg JSON often pads rc34 with trailing `00` (e.g. `10 0F FE 00 00 00`).
 * The bus must use ISO-TP SF **04** `34 10 0F FE`, not **07** with three extra UDS bytes.
 *
 * Reference sequence (GMLAN, physical 0x7E0 / 0x7E8; seed/key varies by ECU):
 *   … → `05 34 00 00 0F FE` → `03 7F 34 78` → `01 74` → `04 34 10 0F FE` (+ CAN padding) → `01 74`
 *   → FF `1F FE 36 00 …` (`36` + `00` + 4-byte addr from rc36 + data) → FC `30 00 F1` → CF `21 …`
 */
function normalizeGmRc34UdsPayload(bytes: number[]): number[] {
  const out = [...bytes];
  while (out.length > 3 && out[out.length - 1] === 0x00) {
    out.pop();
  }
  return out;
}

/** Mirrors DevProg `Common.PROGBYADR` (int order 0..8). */
const enum PrgByAdrMode {
  FALSE = 0,
  STATIC_4 = 1,
  ABS_4 = 2,
  STATIC_5 = 3,
  ABS_5 = 4,
  REL_4 = 5,
  REL_5 = 6,
  CT1BYTE = 7,
  CT2BYTE = 8,
}

const PRG_BY_ADR_BY_NAME: Record<string, PrgByAdrMode> = {
  FALSE: PrgByAdrMode.FALSE,
  STATIC_4: PrgByAdrMode.STATIC_4,
  ABS_4: PrgByAdrMode.ABS_4,
  STATIC_5: PrgByAdrMode.STATIC_5,
  ABS_5: PrgByAdrMode.ABS_5,
  REL_4: PrgByAdrMode.REL_4,
  REL_5: PrgByAdrMode.REL_5,
  CT1BYTE: PrgByAdrMode.CT1BYTE,
  CT2BYTE: PrgByAdrMode.CT2BYTE,
};

function prgByAdrModeName(mode: PrgByAdrMode): string {
  for (const [k, v] of Object.entries(PRG_BY_ADR_BY_NAME)) {
    if (v === mode) return k;
  }
  return String(mode);
}

function parsePrgByAdr(block: ContainerBlockStruct): PrgByAdrMode {
  const raw = getPrgByAdrRawFromDevProgBlock(block);
  if (raw == null || String(raw).trim() === '') {
    return PrgByAdrMode.FALSE;
  }
  const s = String(raw).trim().toUpperCase().replace(/\s/g, '_');
  if (s in PRG_BY_ADR_BY_NAME) {
    return PRG_BY_ADR_BY_NAME[s];
  }
  const n = Number(s);
  if (Number.isInteger(n) && n >= PrgByAdrMode.FALSE && n <= PrgByAdrMode.CT2BYTE) {
    return n as PrgByAdrMode;
  }
  // C# `default:` — single counter byte
  return PrgByAdrMode.CT1BYTE;
}

function u32BigEndian(n: number): Uint8Array {
  const x = n >>> 0;
  return new Uint8Array([
    (x >>> 24) & 0xff, (x >>> 16) & 0xff, (x >>> 8) & 0xff, x & 0xff,
  ]);
}

/** C# `BitConverter` LE + `Reverse()` → big-endian on the wire. */
function prepend00PlusU32Be(n: number): Uint8Array {
  const four = u32BigEndian(n);
  const out = new Uint8Array(5);
  out[0] = 0x00;
  out.set(four, 1);
  return out;
}

/**
 * TransferData (0x36) diagnostic payload **after** the service byte — DevProg `switch(Prgbyadr)`.
 * - `FALSE`: `Rc_36.Skip(1)` (drop leading 0x36 if present), same bytes every chunk.
 * - `STATIC_*`: `Start_adresse` only; `ABS_*`: `Start_adresse + bytesSent`; `REL_*`: `bytesSent` only.
 * - `CT*`: chunk counter `chunkIndex + 1` (same as C# `blockcount + 1`).
 */
function buildGmlanTransferDataHeader(
  block: ContainerBlockStruct,
  mode: PrgByAdrMode,
  bytesSent: number,
  chunkIndex: number,
  allBlocks: ContainerBlockStruct[],
): Uint8Array {
  const start = parseInt(block.start_adresse || '0', 16) >>> 0;

  switch (mode) {
    case PrgByAdrMode.FALSE: {
      const rc = resolveRc36TemplateForBlock(block, allBlocks);
      if (!rc) {
        const keys = Object.keys(block as unknown as Record<string, unknown>).join(', ');
        throw new Error(
          `GMLAN TransferData: PrgByAdr FALSE needs rc36 in block_struct (keys on this block: ${keys || 'none'})`,
        );
      }
      let h = hexToBytes(rc.replace(/^0x/i, ''));
      if (h.length > 0 && h[0] === 0x36) {
        h = h.subarray(1);
      }
      if (h.length === 0) {
        throw new Error('GMLAN TransferData: rc36 empty after Skip(1) / service byte strip');
      }
      return h;
    }
    case PrgByAdrMode.STATIC_4:
      return u32BigEndian(start);
    case PrgByAdrMode.ABS_4:
      return u32BigEndian((start + bytesSent) >>> 0);
    case PrgByAdrMode.STATIC_5:
      return prepend00PlusU32Be(start);
    case PrgByAdrMode.ABS_5:
      return prepend00PlusU32Be((start + bytesSent) >>> 0);
    case PrgByAdrMode.REL_4:
      return u32BigEndian(bytesSent >>> 0);
    case PrgByAdrMode.REL_5:
      return prepend00PlusU32Be(bytesSent >>> 0);
    case PrgByAdrMode.CT1BYTE:
      return new Uint8Array([(chunkIndex + 1) & 0xff]);
    case PrgByAdrMode.CT2BYTE: {
      const c = chunkIndex + 1;
      return new Uint8Array([(c >>> 8) & 0xff, c & 0xff]);
    }
    default:
      return new Uint8Array([(chunkIndex + 1) & 0xff]);
  }
}

