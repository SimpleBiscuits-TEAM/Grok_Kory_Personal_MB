/**
 * Get pressure axis in the requested display unit.
 * If the engine's native unit differs from displayUnit, convert.
 */
export function kpaToMpa(kpa: number): number {
  return kpa / 1000;
}

export function mpaToKpa(mpa: number): number {
  return mpa * 1000;
}

export function kpaToPsi(kpa: number): number {
  return (kpa / 1000) * MPA_TO_PSI;
}

export function psiToKpa(psi: number): number {
  return (psi * PSI_TO_MPA) * 1000;
}

/**
 * Get pressure axis in the requested display unit.
 * Handles all conversions between MPa, PSI, and kPa.
 */
export function getPressureAxisInUnit(engine: EngineConfig, displayUnit: PressureUnit): number[] {
  if (engine.nativePressureUnit === displayUnit) return engine.pressureAxis;
  // First convert to MPa as common base
  const mpas = getPressureAxisMpa(engine);
  if (displayUnit === 'MPa') return mpas;
  if (displayUnit === 'PSI') return mpas.map(mpaToPsi);
  // kPa
  return mpas.map(mpaToKpa);
}

/**
 * Get pressure axis in MPa (normalized for internal math).
 */
/**
 * Get pressure axis in MPa (normalized for internal math).
 */
export function getPressureAxisMpa(engine: EngineConfig): number[] {
  if (engine.nativePressureUnit === 'MPa') return engine.pressureAxis;
  if (engine.nativePressureUnit === 'kPa') return engine.pressureAxis.map(kpaToMpa);
  return engine.pressureAxis.map(psiToMpa);
}
