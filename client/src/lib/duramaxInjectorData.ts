/**
 * Duramax Diesel Injector Duration Tables — All Platforms
 *
 * Each engine has its own stock OEM Main Injection Pulse (or Injection Pulses) table.
 * Tables vary in:
 *   - Pressure axis units (MPa or PSI) and breakpoints
 *   - Fuel quantity axis breakpoints (mm3/stroke)
 *   - Duration values (µs)
 *   - Table ID ({B0720} for LB7/LLY/LBZ/LMM, {B0552} for LML)
 *
 * The converter normalizes everything internally for math, then outputs
 * in the user's chosen display units (imperial PSI default, metric MPa toggle).
 */

// ── Unit conversion constants ──────────────────────────────────────────────
export const MPA_TO_PSI = 145.038;
export const PSI_TO_MPA = 1 / MPA_TO_PSI;

// ── Unified engine config interface ────────────────────────────────────────
