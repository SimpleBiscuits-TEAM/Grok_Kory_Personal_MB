// ==================== TYPES ====================

interface TuneManagerProps {
  currentBinary: Uint8Array | null;
  currentA2L: string | null;
  vehicleInfo: {
    make?: string;
    model?: string;
    year?: number;
    ecuFamily?: string;
    ecuId?: string;
  };
  onTuneLoaded?: (binary: Uint8Array, a2l?: string, metadata?: any) => void;
}

interface FolderNode {
  id: string;
  name: string;
  parentId: string | null;
  folderType: string;
  vehicleMake: string | null;
  vehicleModel: string | null;
  vehicleYear: number | null;
  ecuFamily: string | null;
  isAutoGenerated: boolean | null;
  children: FolderNode[];
  tuneCount?: number;
}

// ==================== FOLDER TREE COMPONENT ====================

function FolderTree({
  folders,
  selectedFolderId,
  onSelectFolder,
  onCreateFolder,
  onDeleteFolder,
  onRenameFolder,
}: {
  folders: FolderNode[];
  selectedFolderId: string | null;
  onSelectFolder: (id: string | null) => void;
  onCreateFolder: (parentId: string | null) => void;
  onDeleteFolder: (id: string) => void;
  onRenameFolder: (id: string, name: string) => void;
}) {
  const [expandedFolders, setExpandedFolders] = useState<Set<string>>(new Set());
  const [renamingId, setRenamingId] = useState<string | null>(null);
  const [renameValue, setRenameValue] = useState('');

  const toggleExpand = (id: string) => {
    setExpandedFolders(prev => {
      const next = new Set(prev);
      if (next.has(id)) next.delete(id);
      else next.add(id);
      return next;
    });
  };

  const startRename = (id: string, currentName: string) => {
    setRenamingId(id);
    setRenameValue(currentName);
  };

  const commitRename = () => {
    if (renamingId && renameValue.trim()) {
      onRenameFolder(renamingId, renameValue.trim());
    }
    setRenamingId(null);
  };

  const getFolderIcon = (folderType: string) => {
    switch (folderType) {
      case 'make': return <Car className="w-3.5 h-3.5 text-red-400" />;
      case 'model': return <Car className="w-3.5 h-3.5 text-orange-400" />;
      case 'year': return <Calendar className="w-3.5 h-3.5 text-yellow-400" />;
      case 'ecu_family': return <Cpu className="w-3.5 h-3.5 text-cyan-400" />;
      case 'ecu_variant': return <Cpu className="w-3.5 h-3.5 text-blue-400" />;
      default: return <Folder className="w-3.5 h-3.5 text-zinc-400" />;
    }
  };

  const renderFolder = (folder: FolderNode, depth: number = 0) => {
    const isExpanded = expandedFolders.has(folder.id);
    const isSelected = selectedFolderId === folder.id;
    const hasChildren = folder.children.length > 0;

    return (
      <div key={folder.id}>
        <div
          className={`flex items-center gap-1 py-1 px-1 rounded cursor-pointer text-[11px] transition-all group ${
            isSelected
              ? 'bg-red-500/20 text-red-300 border-l-2 border-red-500'
              : 'hover:bg-zinc-800/50 text-zinc-400 hover:text-zinc-200 border-l-2 border-transparent'
          }`}
          style={{ paddingLeft: `${depth * 12 + 4}px` }}
          onClick={() => {
            onSelectFolder(folder.id);
            if (hasChildren) toggleExpand(folder.id);
          }}
        >
          {hasChildren ? (
            isExpanded ? <ChevronDown className="w-3 h-3 shrink-0" /> : <ChevronRight className="w-3 h-3 shrink-0" />
          ) : (
            <span className="w-3 shrink-0" />
          )}

          {getFolderIcon(folder.folderType)}

          {renamingId === folder.id ? (
            <input
              value={renameValue}
              onChange={(e) => setRenameValue(e.target.value)}
              onBlur={commitRename}
              onKeyDown={(e) => { if (e.key === 'Enter') commitRename(); if (e.key === 'Escape') setRenamingId(null); }}
              className="flex-1 bg-zinc-800 border border-zinc-600 rounded px-1 text-[11px] text-zinc-200 outline-none"
              autoFocus
              onClick={(e) => e.stopPropagation()}
            />
          ) : (
            <span className="flex-1 truncate">{folder.name}</span>
          )}

          <div className="hidden group-hover:flex items-center gap-0.5">
            <button
              onClick={(e) => { e.stopPropagation(); startRename(folder.id, folder.name); }}
              className="p-0.5 hover:bg-zinc-700 rounded"
              title="Rename"
            >
              <Edit2 className="w-2.5 h-2.5" />
            </button>
            <button
              onClick={(e) => { e.stopPropagation(); onCreateFolder(folder.id); }}
              className="p-0.5 hover:bg-zinc-700 rounded"
              title="New subfolder"
            >
              <FolderPlus className="w-2.5 h-2.5" />
            </button>
            {!folder.isAutoGenerated && (
              <button
                onClick={(e) => { e.stopPropagation(); onDeleteFolder(folder.id); }}
                className="p-0.5 hover:bg-red-900/50 rounded text-red-400"
                title="Delete"
              >
                <Trash2 className="w-2.5 h-2.5" />
              </button>
            )}
          </div>
        </div>

        {isExpanded && folder.children.map(child => renderFolder(child, depth + 1))}
      </div>
    );
  };

  return (
    <div className="space-y-0.5">
      {/* Root / All Tunes */}
      <div
        className={`flex items-center gap-1.5 py-1 px-2 rounded cursor-pointer text-[11px] transition-all ${
          selectedFolderId === null
            ? 'bg-red-500/20 text-red-300 border-l-2 border-red-500'
            : 'hover:bg-zinc-800/50 text-zinc-400 hover:text-zinc-200 border-l-2 border-transparent'
        }`}
        onClick={() => onSelectFolder(null)}
      >
        <Folder className="w-3.5 h-3.5" />
        <span className="font-semibold">All Tunes</span>
      </div>

      {folders.map(folder => renderFolder(folder))}

      {/* New Root Folder button */}
      <button
        onClick={() => onCreateFolder(null)}
        className="flex items-center gap-1.5 py-1 px-2 text-[10px] text-zinc-600 hover:text-zinc-400 transition-all w-full"
      >
        <FolderPlus className="w-3 h-3" />
        New Folder
      </button>
    </div>
  );
}

